    private void TombolSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TombolSimpanActionPerformed
        if(TeksKodepengguna.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Lengkapi Kode Pengguna !");
        }else if(TeksNamapengguna.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Lengkapi Nama Pengguna !");
        }else if(!RadioButtonPria.isSelected() && !RadioButtonWanita.isSelected()){
            JOptionPane.showMessageDialog(null, "Lengkapi Jenis Kelamin !");
        }else if(ComboBoxjabatan.getSelectedItem().toString().equals("")){
            JOptionPane.showMessageDialog(null, "Lengkapi Jabatan !");
        }else if(TeksNomorHP.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Lengkapi Nomor HP !");
        }else if(TeksUsername.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Lengkapi Username !");
        }else if(TeksPassword.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Lengkapi Password !");
        }else{
            try{
                String jk = "";
                if(RadioButtonPria.isSelected()){
                    jk = RadioButtonPria.getText();
                } else if (RadioButtonWanita.isSelected()){
                    jk = RadioButtonWanita.getText();
                }
                
                String sql = "INSERT INTO tb_pengguna VALUES('"+TeksKodepengguna.getText()+"','"+TeksNamapengguna.getText()
                                                            +"','"+jk+"','"+TeksNomorHP.getText()+"','"+ComboBoxjabatan.getSelectedItem()
                                                            +"','"+TeksUsername.getText()+"','"+TeksPassword.getText()
                                                            +"')";
                stat.execute(sql);
                JOptionPane.showMessageDialog(null, "Simpan Data Berhasil!");
                
                bersih();
                freshtabel();
                load_table();
            } catch(Exception e){
                JOptionPane.showMessageDialog(null, "Simpan Data Gagal! "+e.getMessage());
            }
        }
    }//GEN-LAST:event_TombolSimpanActionPerformed


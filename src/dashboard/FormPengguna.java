    private void TombolCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TombolCariActionPerformed
        DefaultTableModel model = (DefaultTableModel) TablePengguna.getModel();
        if(TeksPencarian.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Isi Kolom Pencarian");
            TeksPencarian.requestFocus();
        }else{
            try{
                String sql = "SELECT * FROM tb_pengguna WHERE kd_pengguna LIKE '%"+TeksPencarian.getText()+
                "%' OR nama_pengguna LIKE '%"+TeksPencarian.getText()+"%'";
                freshtabel();
                res=stat.executeQuery(sql);
                while(res.next()){

                    model.addRow(new Object[]{
                        res.getString(1),
                        res.getString(2),
                        res.getString(5),
                        res.getString(6),
                        res.getString(7)
                    });
                }

                TablePengguna.setModel(model);

            } catch(Exception e){

            }
        }
    }//GEN-LAST:event_TombolCariActionPerformed


    private void load_table(){
        DefaultTableModel model = (DefaultTableModel) TablePemasok.getModel();
        try{
            String sql = "SELECT * FROM tb_pemasok";
            res=stat.executeQuery(sql);
            while(res.next()){
                
                model.addRow(new Object[]{
                    res.getString(1),
                    res.getString(2),
                    res.getString(3),
                    res.getString(4)
                });
            }
            TablePemasok.setModel(model);
        } catch(Exception e){
            
        }
    }
    

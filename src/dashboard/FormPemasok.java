    private void TombolCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TombolCariActionPerformed
        DefaultTableModel model = (DefaultTableModel) TablePemasok.getModel();
        if(TeksPencarian.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Isi Kolom Pencarian");
            TeksPencarian.requestFocus();
        }else{
            try{
                String sql = "SELECT * FROM tb_pemasok WHERE kd_pemasok LIKE '%"+TeksPencarian.getText()+
                "%' OR nama_pemasok LIKE '%"+TeksPencarian.getText()+"%'";
                freshtabel();
                res=stat.executeQuery(sql);
                while(res.next()){

                    model.addRow(new Object[]{
                        res.getString(1),
                        res.getString(2),
                        res.getString(3),
                        res.getString(4)
                    });
                }

                TablePemasok.setModel(model);

            } catch(Exception e){

            }
        }
    }//GEN-LAST:event_TombolCariActionPerformed

    private void TombolSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TombolSimpanActionPerformed
        if(TeksKodepemasok.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Lengkapi Kode Pemasok !");
        }else if(TeksNamapemasok.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Lengkapi Nama Pemasok !");
        }else if(TextAreaAlamat.getText().toString().equals("")){
            JOptionPane.showMessageDialog(null, "Lengkapi Alamat !");
        }else if(TeksNomorHP.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Lengkapi Nomor HP !");
        }else{
            try{
                String sql = "INSERT INTO tb_pemasok VALUES('"+TeksKodepemasok.getText()+"','"+TeksNamapemasok.getText()
                                                            +"','"+TextAreaAlamat.getText()+"','"+TeksNomorHP.getText()+"')";
                stat.execute(sql);
                JOptionPane.showMessageDialog(null, "Simpan Data Berhasil!");
                
                bersih();
                freshtabel();
                load_table();
            } catch(Exception e){
                JOptionPane.showMessageDialog(null, "Simpan Data Gagal! "+e.getMessage());
            }
        }
    }//GEN-LAST:event_TombolSimpanActionPerformed

    private void TombolDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TombolDeleteActionPerformed
        if(TeksKodepemasok.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Lengkapi Kode Pemasok yang akan DIHAPUS!");
            TeksKodepemasok.requestFocus();
        }else{
        try{
                String sql = "DELETE FROM tb_pemasok WHERE kd_pemasok = '"+TeksKodepemasok.getText()+"'";
                stat.execute(sql);
                JOptionPane.showMessageDialog(null, "Hapus Data Berhasil!");
                
                bersih();
                freshtabel();
                load_table();
            } catch(Exception e){
                JOptionPane.showMessageDialog(null, "Hapus Data Gagal! "+e.getMessage());
            }
        }
    }//GEN-LAST:event_TombolDeleteActionPerformed

